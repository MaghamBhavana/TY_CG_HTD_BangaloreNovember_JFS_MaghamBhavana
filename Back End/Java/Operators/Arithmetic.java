class Arithmetic
{
	public static void main(String args[])
	{
		int a=10;
		int b=2;
		double c=12.90;
		int d=9;
		int addition=a+b;
		int substraction=a-b;
		double multiplication=c*d;
		double division=d/c;

		System.out.println("Value of addition= "+addition);
		System.out.println("Value of substraction= "+substraction);
		System.out.println("Value of multiplication= "+multiplication);
		System.out.println("Value of division= "+division);
	}
}