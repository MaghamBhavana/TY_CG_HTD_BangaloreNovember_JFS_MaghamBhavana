class Switch
{
	public static void main(String[] args)
	{
		int ch=2;
		switch(ch)
		{
			case 1:System.out.println("I want 1 ice-cream");
			break;
			case 2:System.out.println("I want 2 eggs");
			break;
			case 3:System.out.println("I want 3 buns");
		}

		char s='B';
		switch(s)
		{
			case 'A':System.out.println("I am alphabet A");
			break;
			case 'B':System.out.println("I am alphabet B");
			break;
			case 'C':System.out.println("I am alphabet C");
		}
	}
}