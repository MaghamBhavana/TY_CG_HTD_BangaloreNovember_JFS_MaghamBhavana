package edu.capgemini.stringclass;

public class OwnImmutiableClass
{
	
	private String name;
	private int age;
	public OwnImmutiableClass(String name, int age) 
	{
		super();
		this.name = name;
		this.age = age;
	}
	public String getName() 
	{
		return name;
	}
	
	public int getAge() 
	{
		return age;
	}
	
}
